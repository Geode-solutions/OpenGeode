namespace geode
{
    BRep OpenGeodeBRepInput::read()
    {
        const UnzipFile zip_reader{ filename(), uuid{}.string() };
        zip_reader.extract_all();
        BRep brep;
        detail::load_brep_files( brep, zip_reader.directory() );
        return brep;
    }
} // namespace geode
